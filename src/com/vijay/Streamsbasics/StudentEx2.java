package com.vijay.Streamsbasics;

public class StudentEx2 {

	
	private String name;
	private int age;
	public StudentEx2(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	@Override
	public String toString() {
		return "StudentEx2 [name=" + name + ", age=" + age + "]";
	}
}
